# 每个场景尾帧后要追加的差异硬要求标注
# 空镜用 3 轴,人物用 2 轴
EMPTY_SHOTS = {1, 2, 11, 14, 15, 24, 28}

TAIL_EMPTY = (
    "【⚠️ 首尾帧差异硬要求】首尾帧放一起对比必须一眼看出不同,"
    "相机运动(写死方向/距离/角度)、天气光线变化、动态元素位移,三轴至少命中两轴,"
    "禁止使用任何模糊词如「略」「稍微」「基本」,所有变化必须明确量化。"
)
TAIL_PEOPLE = (
    "【⚠️ 首尾帧差异硬要求】首尾帧放一起必须肉眼可见差异,"
    "相机运动(推/拉/摇/俯仰,写死角度或距离)与人物动作表情变化至少命中一项,"
    "首尾图是动作的两个端点而不是同一瞬间的双胞胎。"
)

# 读、改、写
with open(SRC, 'r', encoding='utf-8-sig') as f:
    reader = csv.DictReader(f)
    fieldnames = reader.fieldnames
    rows = list(reader)

assert len(rows) == 30, f"期望 30 行,实际 {len(rows)}"

modified = 0
for i, r in enumerate(rows, 1):
    if i not in REWRITES:
        continue
    old = r['gpt_prompt']
    # 1. 找到"首帧:"到"【⚠️ 空镜硬性要求】"(如果有)或文末的那一段,把尾帧部分整个替换
    # 原结构一般是 "...首帧:XXX;尾帧:同一机位同一构图,YYY。【⚠️ 空镜硬性要求】..."
    # 或                "...首帧:XXX;尾帧:同一机位同一构图,YYY。"
    new_end = REWRITES[i]

    # 把"尾帧:"开头到下一个句号(。)或【或结尾的一段替换
    pat = re.compile(r'尾帧[::].*?(?=【|$)', re.DOTALL)
    if not pat.search(old):
        print(f"⚠️ 场景 {i} 未找到'尾帧:',跳过")
        continue
    new = pat.sub(new_end + " ", old, count=1)

    # 2. 把老的"【⚠️ 空镜硬性要求】..."整段保留(它本身没问题,只是需要补首尾差异硬要求)
    # 如果场景是空镜,在末尾追加 TAIL_EMPTY;否则 TAIL_PEOPLE
    tail = TAIL_EMPTY if i in EMPTY_SHOTS else TAIL_PEOPLE
    # 避免重复追加
    if "首尾帧差异硬要求" not in new:
        new = new.rstrip() + tail

    # 3. 最终检查:尾帧段里不能再出现禁用词(整段里的"空镜硬性要求"段不检查)
    end_seg_m = re.search(r'尾帧[::](.+?)(?=【|$)', new)
    if end_seg_m:
        end_seg = end_seg_m.group(1)
        for w in ['同一机位', '同一构图']:
            if w in end_seg:
                print(f"❌ 场景 {i} 尾帧段仍有「{w}」!")

    r['gpt_prompt'] = new
    modified += 1

print(f"\n✅ 改写完成:{modified}/30 场景")

# 最终复查:只查尾帧段里是否还有锁死词(整段其他部分可能有"禁用词列表"是 OK 的)
print("\n--- 最终复查(只看每条尾帧描述段)---")
for w in ['同一机位', '同一构图', '同一角度', '只有细微变化']:
    hits = 0
    for r in rows:
        m = re.search(r'尾帧[::](.+?)(?=【|$)', r.get('gpt_prompt',''))
        if m and w in m.group(1):
            hits += 1
    print(f"  尾帧段里的「{w}」: {hits} 场景(应为 0)")

# 写回
with open(DST, 'w', encoding='utf-8-sig', newline='') as f:
    writer = csv.DictWriter(f, fieldnames=fieldnames, quoting=csv.QUOTE_ALL)
    writer.writeheader()
    writer.writerows(rows)

print(f"\n📝 已写入 {DST}")
